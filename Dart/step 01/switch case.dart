void main(){
    // makes it convenient to decide between many different possibilities
    String favouriteAnimal = "cat";

    switch(favouriteAnimal){
    case "dog":
        print("dog is my favourite animal ever");
    break;
    case "cat":
        print("cat is my favourite animal ever");
    break;
    case "cow":
        print("cow is my favourite animal ever");
    break;
    default:
        print("something else is my favourite animal");
}
}